namespace Luna.Mobile.Controls;

/// <summary>
/// 定义 <see cref="Popover" /> 的弹出位置。
/// </summary>
public enum PopoverPlacement
{
    /// <summary>
    /// 顶部居中。
    /// </summary>
    Top,
    /// <summary>
    /// 顶部左对齐。
    /// </summary>
    TopLeft,
    /// <summary>
    /// 顶部右对齐。
    /// </summary>
    TopRight,
    /// <summary>
    /// 底部居中。
    /// </summary>
    Bottom,
    /// <summary>
    /// 底部左对齐。
    /// </summary>
    BottomLeft,
    /// <summary>
    /// 底部右对齐。
    /// </summary>
    BottomRight,
    /// <summary>
    /// 左侧居中。
    /// </summary>
    Left,
    /// <summary>
    /// 左侧顶部对齐。
    /// </summary>
    LeftTop,
    /// <summary>
    /// 左侧底部对齐。
    /// </summary>
    LeftBottom,
    /// <summary>
    /// 右侧居中。
    /// </summary>
    Right,
    /// <summary>
    /// 右侧顶部对齐。
    /// </summary>
    RightTop,
    /// <summary>
    /// 右侧底部对齐。
    /// </summary>
    RightBottom,
}
